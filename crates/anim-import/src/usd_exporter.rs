    fn test_roundtrip() {
        let model = ImportedModel {
            name: "RoundTrip".into(),
            meshes: Vec::new(),
            skin: None,
            joint_names: vec!["Root".into(), "Child".into()],
            parent_indices: vec![-1, 0],
            animation_frames: None,
        };
        let frames = vec![
            vec![Mat4::IDENTITY, Mat4::from_translation(Vec3::new(0.0, 1.0, 0.0))],
            vec![
                Mat4::from_translation(Vec3::new(1.0, 0.0, 0.0)),
                Mat4::from_translation(Vec3::new(1.0, 1.5, 0.0)),
            ],
        ];
        let dir = std::env::temp_dir();
        let path = dir.join("test_roundtrip.usda");

        // Export
        export_usd(&path, &model, Some(&frames), 24.0).unwrap();

        // Import back
        let imported = import_usd(&path).unwrap();
        assert_eq!(imported.joint_names.len(), 2);
        assert_eq!(imported.joint_names[0], "Root");
        assert_eq!(imported.joint_names[1], "Child");
        assert_eq!(imported.parent_indices[0], -1);
        assert_eq!(imported.parent_indices[1], 0);

        // Animation should have been imported
        let anim = imported.animation_frames.as_ref().expect("animation should exist");
        assert_eq!(anim.frames.len(), 2); // 2 frames
        assert_eq!(anim.frames[0].len(), 2); // 2 joints

        // Check root transform in frame 1 — should be near (1, 0, 0) translation
        let root_f1 = anim.frames[1][0];
        let root_pos = Vec3::new(root_f1.w_axis.x, root_f1.w_axis.y, root_f1.w_axis.z);
        assert!((root_pos - Vec3::new(1.0, 0.0, 0.0)).length() < 0.01,
            "Root position at frame 1 should be near (1,0,0), got {:?}", root_pos);

        std::fs::remove_file(&path).ok();
    }

